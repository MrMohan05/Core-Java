package practice;

public class Conversion 
{
	public static void main(String args[])
	{
		int d=100;//Decimal
		System.out.println("In Decimal="+d);
		
		int b=0b100;//Binary
		System.out.println("In Binary="+b);
		
		int o=0100;//Octal
		System.out.println("In Octal="+o);
		
		int x=0x1a;//Hexadecimal
		System.out.println("In Hexadecimal="+x);
	}
}
